const iconNodes = JSON.parse(fs.readFileSync(iconNodesPath, "utf8"));
const iconNames = Object.keys(iconNodes).sort((a, b) => a.localeCompare(b));

function attrToKebabCase(name) {
  return name.replace(/[A-Z]/g, (match) => `-${match.toLowerCase()}`);
}

function escapeAttribute(value) {
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/"/g, "&quot;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;");
}

function nodeToMarkup(node) {
  const [tag, attributes] = node;
  const attrText = Object.entries(attributes)
    .map(
      ([name, value]) => `${attrToKebabCase(name)}="${escapeAttribute(value)}"`
    )
    .join(" ");
  return `<${tag}${attrText ? ` ${attrText}` : ""}></${tag}>`;
}

function humanLabel(name) {
  return name.replace(/-/g, " ");
}

function jsObject(entries, valueFactory) {
  return entries
    .map(
      (name) =>
        `  ${JSON.stringify(name)}: ${JSON.stringify(valueFactory(name))}`
    )
    .join(",\n");
}

const iconPathEntries = jsObject(iconNames, (name) =>
  iconNodes[name].map(nodeToMarkup).join("")
);

const catalogEntries = iconNames
  .map((name) => {
    const entry = {
      id: `lucide-${name}`,
      iconName: name,
      set: "lucide",
      name,
      humanLabel: humanLabel(name)
    };
    return `  ${JSON.stringify(name)}: ${JSON.stringify(entry)}`;
  })
  .join(",\n");

